const messages: Record<Locale, AppText> = { en, zh };

export function detectLocale(languages = navigator.languages): Locale {
  const candidates = languages.length > 0 ? languages : [navigator.language];
  for (const language of candidates) {
    const normalized = language.toLowerCase();
    if (normalized.startsWith("zh")) return "zh";
    if (normalized.startsWith("en")) return "en";
  }
  return "en";
}

export function getBrowserMessages(): AppText {
  return messages[detectLocale()];
}
